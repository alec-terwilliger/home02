    private void newGameButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newGameButtonActionPerformed
        Random rand = new Random();
        String firstPlayer = JOptionPane.showInputDialog(null,"First Player's Name");
        String secondPlayer = JOptionPane.showInputDialog(null,"Second Player's Name");
        
                
        player1 = new Player(firstPlayer);
        player2 = new Player(secondPlayer);
        gameNim = new Game(player1, player2, 21);
        player1Label.setText(firstPlayer);
        player2label.setText(secondPlayer);
        int whoStarts = rand.nextInt(2 - 1 + 1) + 1;
        if (whoStarts == 1) {
            p1t1Button.setEnabled(true);
            p1t2Button.setEnabled(true);
            p1t3Button.setEnabled(true);
            p2t1Button.setEnabled(false);
            p2t2Button.setEnabled(false);
            p2t3Button.setEnabled(false);
            textArea.setText("It is " + firstPlayer + "'s turn. \n");
        }
        else
        {
            p2t1Button.setEnabled(true);
            p2t2Button.setEnabled(true);
            p2t3Button.setEnabled(true);
            p1t1Button.setEnabled(false);
            p1t2Button.setEnabled(false);
            p1t3Button.setEnabled(false);
            textArea.setText("It is " + secondPlayer + "'s turn. \n");
        }
        repaint();
        
    }//GEN-LAST:event_newGameButtonActionPerformed

    
    
