    private void p1t1ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_p1t1ButtonActionPerformed
        gameNim.removeFromPile(1);
        textArea.setText(textArea.getText() + "\n" + player1.getName() + " took 1 marble \nThere are " + gameNim.getMarblesRemaining() + " marbles remaining. \nIt is " +
                player2.getName() + "'s turn \n");
        marblesRemainingLabel.setText(gameNim.getMarblesRemaining() + "");
        if(gameNim.getMarblesRemaining() < 1)
        {
            p2t1Button.setEnabled(false);
            p2t2Button.setEnabled(false);
            p2t3Button.setEnabled(false);
            textArea.setText(textArea.getText() + "\n" + player1.getName() + " is the winner!");
        }
        else
        {
           p2t1Button.setEnabled(true); 
        }
        
        if (gameNim.getMarblesRemaining() < 2) 
        {
            p2t2Button.setEnabled(false);
        }
        else
        {
        p2t2Button.setEnabled(true);
        }
        if(gameNim.getMarblesRemaining() < 3){
            p2t3Button.setEnabled(false);
        }
        else
        {
            p2t3Button.setEnabled(true);
        }
        
        p1t1Button.setEnabled(false);
        p1t2Button.setEnabled(false);
        p1t3Button.setEnabled(false);
        
        repaint();
    }//GEN-LAST:event_p1t1ButtonActionPerformed

